package cn.cychee.objectadapter;

public class Adaptee {
    private String string;

    public Adaptee(String string) {
        this.string = string;
    }

    public void showWithParen() {
        System.out.println("(" + string + ")");
    }

    public void showWithAster() {
        System.out.println("*" + string + "*");
    }
}
